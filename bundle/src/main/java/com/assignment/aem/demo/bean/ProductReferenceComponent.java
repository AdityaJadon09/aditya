package com.assignment.aem.demo.bean;

public class ProductReferenceComponent {
	
	private String Name;
	
	private String Label;
	
	private String type;
	
	private String isBoldRequired;
	
	private String date;
	
	private String fieldvalue;
	
	private String locale;
	
	private String isAlwaysVisible;
	
	private String time;
	
	public String getIsAlwaysVisible() {
		return isAlwaysVisible;
	}

	public void setIsAlwaysVisible(String isAlwaysVisible) {
		this.isAlwaysVisible = isAlwaysVisible;
	}

	public String getLocale() {
		return locale;
	}

	public void setLocale(String locale) {
		this.locale = locale;
	}

	public String getFieldvalue() {
		return fieldvalue;
	}

	public void setFieldvalue(String fieldvalue) {
		this.fieldvalue = fieldvalue;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getLabel() {
		return Label;
	}

	public void setLabel(String label) {
		Label = label;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getIsBoldRequired() {
		return isBoldRequired;
	}

	public void setIsBoldRequired(String isBoldRequired) {
		this.isBoldRequired = isBoldRequired;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	
		

}
